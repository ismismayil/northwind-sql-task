INNER JOIN HR.EMPLOYEES e ON j.EMPLOYEE_ID = e.EMPLOYEE_ID
INNER JOIN HR.DEPARTMENTS d ON e.DEPARTMENT_ID = d.DEPARTMENT_ID
INNER JOIN HR.LOCATIONS l ON d.LOCATION_ID = l.LOCATION_ID;

