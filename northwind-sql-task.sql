SELECT MANAGER_ID AS CustomerID, EMPLOYEE_ID AS OrderID, HIRE_DATE AS OrderDate,
ROW_NUMBER() OVER (PARTITION BY MANAGER_ID ORDER BY HIRE_DATE) AS CustomerOrderSeq
FROM HR.EMPLOYEES;

--2:
SELECT DEPARTMENT_ID AS CategoryID, FIRST_NAME AS ProductName, SALARY AS UnitPrice,
RANK() OVER (PARTITION BY DEPARTMENT_ID ORDER BY SALARY DESC) AS PriceRank
FROM HR.EMPLOYEES;

--3:
SELECT EMPLOYEE_ID AS OrderID, HIRE_DATE AS OrderDate, SALARY AS Freight,
SUM(SALARY) OVER (ORDER BY HIRE_DATE, EMPLOYEE_ID) AS RunningTotalFreight
FROM HR.EMPLOYEES;

