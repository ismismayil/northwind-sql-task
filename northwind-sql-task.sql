--2:
WITH CustomerSales AS (
    SELECT e.MANAGER_ID AS CustomerID, SUM(e.SALARY * NVL(e.COMMISSION_PCT, 1)) AS TotalSpent
    FROM HR.EMPLOYEES e
    GROUP BY e.MANAGER_ID
)
SELECT m.FIRST_NAME AS CompanyName, cs.TotalSpent FROM CustomerSales cs
JOIN HR.EMPLOYEES m ON cs.CustomerID = m.EMPLOYEE_ID
ORDER BY cs.TotalSpent DESC;

