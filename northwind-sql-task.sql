INNER JOIN HR.DEPARTMENTS d ON j.DEPARTMENT_ID = d.DEPARTMENT_ID
INNER JOIN HR.EMPLOYEES e ON j.EMPLOYEE_ID = e.EMPLOYEE_ID

--2:
